    private void atualizarTotal() {
    float total = 0;
    for (int i = 0; i < listaCompras.getRowCount(); i++) {
        total += (Float) listaCompras.getValueAt(i, 3);
    }
    contagemPreco = total;
    label_total.setText(" TOTAL: R$" + String.format("%.2f", contagemPreco));
}
    
